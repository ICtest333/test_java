public class OperatorEx05 {
	public static void main(String[] args){
		int a = 5;
		int b = 7;
		
		a += b; // a = a + b
		System.out.println("a += b : " + a);
		
		a -= b; // a = a - b
		System.out.println("a -= b : " + a);
		
		a *= b; // a = a * b
		System.out.println("a *= b : " + a);
		
		a /= b; // a = a / b
		System.out.println("a /= b : " + a);
		
		a %= b; // a = a % b
		System.out.println("a %= b : " + a);
	}
}
